public class SideEffect {
    public String name;
    public int level;
    public int level2;
    public String question1;
    public String question2;

    public SideEffect(String name, int level, int level2, String question1, String question2){
        super();
        this.name =  name;
        this.level = level;
        this.level2 = level2;
        this.question1 = question1;
        this.question2 = question2;
    }
}